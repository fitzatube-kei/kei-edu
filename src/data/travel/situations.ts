// Helper functions
export const getSituationsByLocation = (locationId: string): TravelSituation[] => {
  return allSituations.filter(sit => sit.locationId === locationId);
};

export const getSituationById = (id: string): TravelSituation | undefined => {
  return allSituations.find(sit => sit.id === id);
};

export const getRandomSituation = (locationId: string, completedIds: string[]): TravelSituation | undefined => {
  const available = allSituations.filter(
    sit => sit.locationId === locationId && !completedIds.includes(sit.id)
  );
  if (available.length === 0) return undefined;
  return available[Math.floor(Math.random() * available.length)];
};
